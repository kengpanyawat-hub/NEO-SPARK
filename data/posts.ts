// ===== Helper: เดา category จาก tags ตัวแรก =====
function deriveCategory(tags: string[] = []): Cat {
  const t = (tags[0] || "").toLowerCase();
  if (
    t.includes("design") || t.includes("ดีไซน์") || t.includes("กราฟิก") || t.includes("graphic")
  ) return "Design";
  if (
    t.includes("web") || t.includes("เว็บไซต์") || t.includes("site") || t.includes("next.js") || t.includes("seo")
  ) return "Web";
  if (
    t.includes("video") || t.includes("วิดีโอ") || t.includes("motion") || t.includes("โมชัน") || t.includes("edit")
  ) return "Video";
  if (
    t.includes("ads") || t.includes("โฆษณา") || t.includes("ad") || t.includes("campaign")
  ) return "Ads";
  if (
    t.includes("event") || t.includes("อีเวนต์") || t.includes("งานอีเวนต์")
  ) return "Event";
  return "Marketing";
}

